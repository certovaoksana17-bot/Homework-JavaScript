//Хранение данных по ссылке(сложные значения)

let user = { name: 'Ольга', age: 25 } // присваиваем значение обьекта переменной 
const admin = user //обьект стал некой ссылкой, присваиваем(делимся ссылкой) значение другой переменной

// потом переопределяем первую переменную на другой обьект
user = { name: 'Паша' }

console.log(admin) // выводим в консоль вторую переменную и она выводит именно тот обьект который мы присвоили изначально
console.log(user) //первая переменная выводит новый обьект
// когда  переменная присваивает значение нового обьекта, то теряет ссылку на прошлый обьект и изменения на него не будут влиять.
