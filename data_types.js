//В примитивных значениях переменная хранится как она есть

let numberOne = 7 //присваиваем значениe первой переменной
let numberTwo = numberOne //а второй переменной присваиваем значение первой

numberTwo = 9 // потом мы присваиваем значение второй переменной второе значение

 console.log(numberTwo) //при выведении в консоль мы получаем, что второе значение переопределилось во второе значение
 console.log(numberOne) 

